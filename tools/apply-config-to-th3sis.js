/*
 * apply-config-to-th3sis.js
 * Takes a palette-builder `custom@@slot@@mode` config and emits TH3SIS's theme.css.
 *
 * The config speaks the slot schema (crust…text + accent). TH3SIS speaks its own
 * names (--page-bg, --tx1…tx9, --blue-soft). This file is the MAP between them —
 * one slot can feed several TH3SIS vars (SPEC §3: bind by role, not by color).
 *
 * Run:  node tools/apply-config-to-th3sis.js > /path/to/stocks/theme.css
 */

// ── The config you liked (paste a new one here to retheme everything) ──────
const CONFIG = {
  // Light = warm "old paper" (Rosé Pine cream surfaces + espresso text), orange accent.
  "custom@@crust@@light": "#f4ece0", "custom@@mantle@@light": "#efe5d4", "custom@@base@@light": "#fdf8ef",
  "custom@@surface0@@light": "#ebdcc9", "custom@@surface1@@light": "#e2d3bf", "custom@@surface2@@light": "#d6c8b4",
  "custom@@overlay0@@light": "#b6a994", "custom@@overlay1@@light": "#968a76", "custom@@overlay2@@light": "#756a58",
  "custom@@subtext0@@light": "#4a4338", "custom@@subtext1@@light": "#342e25", "custom@@text@@light": "#211c15",
  "custom@@accent@@light": "#ac5700",
  "custom@@crust@@dark": "#051214", "custom@@mantle@@dark": "#0b191b", "custom@@base@@dark": "#112022",
  "custom@@surface0@@dark": "#1b292c", "custom@@surface1@@dark": "#253336", "custom@@surface2@@dark": "#314043",
  "custom@@overlay0@@dark": "#49595b", "custom@@overlay1@@dark": "#5f6f72", "custom@@overlay2@@dark": "#798a8d",
  "custom@@subtext0@@dark": "#9dafb2", "custom@@subtext1@@dark": "#b9cbce", "custom@@text@@dark": "#d9ecef",
  "custom@@accent@@dark": "#f2a26a",
};

// slots[mode][slot] = hex
const slots = { light:{}, dark:{} };
for (const [k,v] of Object.entries(CONFIG)) {
  const [, slot, mode] = k.split("@@");        // "custom","base","dark"
  if (slots[mode]) slots[mode][slot] = v;
}

// ── The MAP: TH3SIS var → slot (binds by UI role). Works in both modes. ────
const MAP = {
  // surfaces (dark→light is handled by the slot values themselves)
  "page-bg":"crust", "nav-bg":"mantle", "inner-bg":"mantle", "panel-bg":"base",
  "tiny-bg":"base", "row-hl":"base", "deep-bg":"surface0", "tag-bg":"surface0", "ghost":"surface1",
  // borders
  "bd":"surface1", "bd2":"surface2", "bd3":"overlay0", "wrap-bd":"surface1", "tip-bd":"overlay0",
  // text ramp (tx1 strongest → tx9 faintest)
  "tx1":"text", "tx2":"subtext1", "tx3":"subtext0", "tx4":"subtext0", "tx5":"overlay2",
  "tx6":"overlay1", "tx7":"overlay1", "tx8":"overlay0", "tx9":"overlay0",
  "title":"text", "track-line":"text", "tag-tx":"subtext1",
  // accent
  "blue-soft":"accent",
};

// structural tokens derived from mapped values (gradients/shadow/opacity)
function structural(mode, get){
  const shadow = mode==="dark" ? "0 40px 120px rgba(0,0,0,.6)" : "0 40px 120px rgba(80,55,30,.09)";
  return {
    "wrap-bg":     `linear-gradient(160deg, ${get("nav-bg")}, ${get("page-bg")})`,
    "wrap-shadow": shadow,
    "tip-bg":      `linear-gradient(160deg, ${get("ghost")}, ${get("inner-bg")})`,
    "grain-op":    mode==="dark" ? ".025" : "0",
  };
}

const ORDER = [
  "page-bg","wrap-bg","wrap-bd","wrap-shadow","panel-bg","inner-bg","nav-bg","deep-bg","tiny-bg",
  "bd","bd2","bd3","tx1","tx2","tx3","tx4","tx5","tx6","tx7","tx8","tx9",
  "title","tag-bg","tag-tx","track-line","blue-soft","tip-bg","tip-bd","grain-op","row-hl","ghost",
];

function block(mode){
  const direct = {};
  for (const [v, slot] of Object.entries(MAP)) direct[v] = slots[mode][slot];
  const get = n => direct[n];
  const all = { ...direct, ...structural(mode, get) };
  const sel = mode==="dark" ? ":root" : ":root.light";
  return `${sel} {\n` + ORDER.map(n => `  --${n}: ${all[n]};`).join("\n") + `\n}`;
}

process.stdout.write(
`/*
 * TH3SIS — Shared theme variables  [GENERATED by palette-builder]
 * Source config + map: tools/apply-config-to-th3sis.js. Edit the CONFIG there, regenerate.
 */

${block("dark")}

${block("light")}
`);
